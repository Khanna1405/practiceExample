public class floatDouble {
    public static void main(String[] args) {
       int myInt=10/3;
        System.out.println("my Int is :"+ myInt);
       float myfloat=1.25f/3f;
        System.out.println("myfloat is:"+ myfloat);
        double mydouble=10.25/3;
        System.out.println("mydouble:"+ mydouble);
    }
}

//here we can see that if we are not using 'd' word for double even then by default
//it will consider the number as double only.